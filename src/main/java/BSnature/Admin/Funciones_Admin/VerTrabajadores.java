    private void botonBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBajaActionPerformed
       int filas = 0;
       try{
        String usuario = JOptionPane.showInputDialog(this, "Introduce aqui el nombre del usuario:").trim();
        if(usuario == null){
            JOptionPane.showMessageDialog(this, "El usuario no puede estar vacio", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else{
                TrabajadorDAO t1 = new TrabajadorDAO();
                filas = t1.eliminarTrabajador(usuario);
        }
       }catch(NullPointerException e){}
        JDialog dialog = new JDialog();
        dialog.setTitle("Eliminacion");
        dialog.setSize(200, 175);
        dialog.setLayout(new FlowLayout());
        dialog.add(new JLabel("Han sido eliminadas " +  filas + " filas"));
        dialog.setVisible(true);
        dialog.toFront();
    }//GEN-LAST:event_botonBajaActionPerformed

    private void BotonCambiarContraseñaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonCambiarContraseñaActionPerformed
        int filas = 0;
        try{
            String usuario = JOptionPane.showInputDialog(this, "Introduce aqui el nombre del usuario:").trim();
        String contraseña = JOptionPane.showInputDialog(this, "Introduce la contrseña nueva:").trim();
        if(usuario != null && contraseña != null){
            try{
                Connection conn = conectarBD.conectarBD();
                String sql = "UPDATE TRABAJADOR SET CONTRASEÑA = ? WHERE USUARIO = ?";
                PreparedStatement stmt = conn.prepareStatement(sql);
                stmt.setString(1, contraseña);
                stmt.setString(2, usuario);
                filas = stmt.executeUpdate();
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this, "Ha habido un error en el sistema", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
            JDialog dialog = new JDialog();
            dialog.setTitle("Actualizacion");
            dialog.setSize(200, 175);
            dialog.setLayout(new FlowLayout());
            dialog.add(new JLabel("Han sido actualizadas " +  filas + " contraseñas"));
            dialog.setVisible(true);
            dialog.toFront();
        }
        }catch(NullPointerException e){}
    }//GEN-LAST:event_BotonCambiarContraseñaActionPerformed

