    private void settearLista() {
       try{
           Connection conn = conectarBD.conectarBD();
           String sql = "SELECT * FROM TRABAJADOR WHERE ES_ADMIN = 0 AND ES_ACTIVO = 1";
           PreparedStatement stmt = conn.prepareStatement(sql);
           ResultSet rs = stmt.executeQuery();
           areaTexto.setText("");
           while(rs.next()){
               String res = rs.getString("NOMBRE") + ", usuario: " + rs.getString("USUARIO") + ", contratado el " + rs.getDate("FECHA_CONTRATACION");
               areaTexto.setText(areaTexto.getText() + "\n" + res);
           }
       }catch(SQLException e){
           System.out.println(e.getMessage());
       }
    }
